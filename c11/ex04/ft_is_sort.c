int	ft_is_sort(int *tab, int length, int (*f) (int, int))
{
	int	i;
	int	a;
	int	d;

	i = -1;
	a = 0;
	d = 0;
	while (++i < length - 1)
	{
		if (f(tab[i], tab[i + 1]) >= 0)
			a++;
		if (f(tab[i], tab[i + 1]) <= 0)
			d++;
	}
	if (i == a || i == d)
		return (1);
	return (0);
}
